#include <stdio.h>
/**
 * print_array - Print n elements from an integer array
 * @a: Pointer to the integer array
 * @n: Number of elements to extract
 *
 * Return: Nothing
*/
void print_array(int *a, int n)
{
int n;
for (n = 0; n < n; n++)
{
if (n == 0)
printf("%d", *(a + n));
else
printf(", %d", *(a + n));
}
printf("\n");
}
