#include "3-calc.h"
/**
 * op_add - sum
 * @a:num1
 * @b:num2
 */
int op_add(int a, int b)
{
return (a + b);
}
 /**
* op_sub - sub
* @a:num1
* @b:num2
*/
int op_sub(int a, int b)
{
return (a - b);
}
 /**
* op_mul - mul
* @a:num1
* @b:num2
*/
int op_mul(int a, int b)
{
return (a * b);
}
 /**
* op_div - div
* @a:num1
* @b:num2
*/
int op_div(int a, int b)
{
return (a / b);
}
 /**
* op_mod - mod
* @a:num1
* @b:num2
*/
int op_mod(int a, int b)
{
return (a % b);
}
